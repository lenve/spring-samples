public class Person {
    private String username;

    @Override
    public String toString() {
        return "Person{" +
                "username='" + username + '\'' +
                '}';
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }
}
