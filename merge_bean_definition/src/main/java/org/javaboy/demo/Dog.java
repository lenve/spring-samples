public class Dog {
    private String category;
    private String name;

    @Override
    public String toString() {
        return "Dog{" +
                "category='" + category + '\'' +
                ", name='" + name + '\'' +
                '}';
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
